/*
*********************************************************************************************************
*                                            BSP_Ser_Init()
*
* Description : Initialize the board's serial communications
*
* Argument(s) : baud rate as integer bits per second
*
* Return(s)   : none.
*
* Caller(s)   : App_TaskStart()
*
* Note(s)     : none.
*********************************************************************************************************
*/

void BSP_Ser_Init(CPU_INT32U baud)
{
    SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOA);

    GPIOPinConfigure(GPIO_PA0_U0RX);
    GPIOPinConfigure(GPIO_PA1_U0TX);
    GPIOPinTypeUART(GPIO_PORTA_BASE, GPIO_PIN_0 | GPIO_PIN_1);

    BSP_IntVectSet(BSP_INT_ID_UART0, UARTStdioIntHandler);

    UARTStdioInitExpClk(0, baud);

}

